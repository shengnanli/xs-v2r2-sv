// =============================================================================
// regfile_pkg —— 物理寄存器堆(Physical Register File)参数与类型
// -----------------------------------------------------------------------------
// 香山 V2R2 的整数/浮点物理寄存器堆由生成器 Regfile.scala 产出。为降低单个寄存器
// 阵列的端口(读/写)宽度、便于物理实现(布线/面积/时序),寄存器堆按数据位"竖切"成
// 若干 Part:每个 PartN 只保存全宽数据的 1/splitNum 个 bit 切片,所有 Part 同地址、
// 同读写口并行工作,顶层把各 Part 的读出再 Cat 拼回全宽。
//
//   全宽 64-bit 整数寄存器  ──split=4──>  IntRegFilePart0..3,每片 16-bit
//   全宽 64-bit 浮点寄存器  ──split=4──>  FpRegFilePart0..3, 每片 16-bit
//
// 因此 8 个 PartN 模块结构完全相同,仅参数不同,可用同一套可读核覆盖:
//   - Int:224 个物理寄存器,11 读口,8 写口,32 个 debug 读口,hasZero=true。
//   - Fp :192 个物理寄存器,11 读口,6 写口,32 个 debug 读口,hasZero=false。
//
// 设计要点(对应 Scala `class Regfile`):
//   * 读口为"同步读":第 1 拍寄存读地址,第 2 拍用寄存后的地址查 memForRead 组合出数据
//     (即读延迟 1 拍)。这是为面积/时序而做的寄存器堆典型结构。
//   * 写口为多写口:每个物理寄存器对各写口做 one-hot 命中(地址比较+wen),用 Mux1H
//     选出写数据;同一拍写读不旁路(读看到的是旧值,因为读经过了 1 拍地址寄存)。
//   * hasZero:整数堆物理寄存器 0 恒为 0(x0 语义,写入被丢弃);浮点堆没有恒零寄存器。
//   * debug 读口为"异步读":直接用当拍地址查 memForRead,延迟 0 拍,仅供 difftest/调试。
//   * memForRead 是一张 256 项查找表(地址 8-bit):前 numPregs 项是寄存器本体,
//     超出 numPregs 的高地址项为 padding(Int 填 0;Fp 填 mem[0],与 golden 对齐),
//     正常运行地址不会越过 numPregs。
// =============================================================================
package regfile_pkg;

  // 读口数据相对写入的延迟:读地址寄存 1 拍后再查表。
  localparam int unsigned RF_READ_LATENCY = 1;

endpackage : regfile_pkg
